package com.example.model;  
  
public class Customer {  
    private int id;  
    private String name;  
  
    // ゲッタ・セッタ  
    public int getId() {  
        return id;  
    }  
    public void setId(int id){  
        this.id = id;  
    }  
    public String getName(){  
        return name;  
    }  
    public void setName(String name){  
        this.name = name;  
    }  
}
